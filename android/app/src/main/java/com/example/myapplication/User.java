package com.example.myapplication;

public class User {
    private String profile;
    private String id;
    private int pw;
    private String userName;

    public User(){}

    public String getProfile() {
        return profile;
    }

    public String getId() {
        return id;
    }

    public int getPw() {
        return pw;
    }

    public String getUserName() {
        return userName;
    }
}
